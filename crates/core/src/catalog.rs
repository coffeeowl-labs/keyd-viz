//! The curated library of common physical layouts, baked in as coordinate tables.
//!
//! keyd configs carry no geometry (ROADMAP §4.5), so physical positions live here.
//! Each layout is a flat `(x, y, w, h, keyd-name)` table in key units, transcribed from
//! QMK's canonical community-layout definitions (`layouts/default/<name>/info.json`)
//! zipped with the standard keyd-name sequence for that layout — so the data is exact
//! and fully self-contained (no runtime JSON, no network). The QMK importer
//! ([`crate::qmk`]) remains the escape hatch for boards outside this set.
//!
//! A few caps carry firmware-only legends (`fn`, `lower`, `raise`) that aren't real
//! keyd keys; they render truthfully but never match a keyd binding or a live keypress.

use crate::geometry::{Geometry, Slot};

/// One pickable layout: a stable `id` (used in the CLI, persistence, and the picker)
/// and a human display `name`.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct BoardKind {
    pub id: &'static str,
    pub name: &'static str,
}

/// The curated layouts offered in the picker, in display order.
pub static LIST: &[BoardKind] = &[
    BoardKind { id: "ansi60", name: "ANSI 60%" },
    BoardKind { id: "iso60", name: "ISO 60%" },
    BoardKind { id: "hhkb", name: "HHKB" },
    BoardKind { id: "65", name: "65%" },
    BoardKind { id: "tkl", name: "TKL" },
    BoardKind { id: "full", name: "Full-size" },
    BoardKind { id: "ortho", name: "Ortho 4\u{00d7}12" },
];

/// All curated layouts, in display order.
pub fn list() -> &'static [BoardKind] {
    LIST
}

/// The display name for a layout id, if known.
pub fn name(id: &str) -> Option<&'static str> {
    LIST.iter().find(|b| b.id == id).map(|b| b.name)
}

/// Build the positioned [`Geometry`] for a layout id; `None` if the id is unknown.
pub fn geometry(id: &str) -> Option<Geometry> {
    let table = match id {
        "ansi60" => ANSI60,
        "iso60" => ISO60,
        "hhkb" => HHKB,
        "65" => K65,
        "tkl" => TKL,
        "full" => FULL,
        "ortho" => ORTHO,
        _ => return None,
    };
    Some(from_table(table))
}

/// Guess a sensible default layout id from a config file name — the interim default
/// until the user picks (and we persist) a layout per keyboard. Most specific first.
pub fn guess(path: &str) -> &'static str {
    let n = path.rsplit('/').next().unwrap_or(path).to_ascii_lowercase();
    if n.contains("hhkb") {
        "hhkb"
    } else if n.contains("ortho") || n.contains("planck") {
        "ortho"
    } else if n.contains("tkl") {
        "tkl"
    } else if n.contains("full") || n.contains("104") {
        "full"
    } else if n.contains("iso") {
        "iso60"
    } else if n.contains("65") {
        "65"
    } else {
        "ansi60"
    }
}

/// `(x, y, w, h, keyd-name)` for one key, in key units. An empty name marks a
/// decorative slot (rendered blank, matches no binding).
type Cell = (f32, f32, f32, f32, &'static str);

fn from_table(t: &[Cell]) -> Geometry {
    let slots = t
        .iter()
        .map(|&(x, y, w, h, n)| Slot {
            x,
            y,
            w,
            h,
            r: 0.0,
            rx: 0.0,
            ry: 0.0,
            key: if n.is_empty() { None } else { Some(n.to_string()) },
        })
        .collect();
    Geometry { slots }
}

