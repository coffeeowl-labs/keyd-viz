#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn every_listed_layout_builds() {
        for b in list() {
            let g = geometry(b.id).unwrap_or_else(|| panic!("no geometry for {}", b.id));
            assert!(!g.slots.is_empty(), "{} is empty", b.id);
            let (w, h) = g.extent();
            assert!(w > 0.0 && h > 0.0, "{} has zero extent", b.id);
        }
    }

    #[test]
    fn unknown_id_is_none() {
        assert!(geometry("nope").is_none());
        assert!(name("nope").is_none());
    }

    #[test]
    fn expected_key_counts() {
        // counts from QMK's canonical community layouts
        assert_eq!(geometry("ansi60").unwrap().slots.len(), 61);
        assert_eq!(geometry("iso60").unwrap().slots.len(), 62);
        assert_eq!(geometry("hhkb").unwrap().slots.len(), 60);
        assert_eq!(geometry("65").unwrap().slots.len(), 68);
        assert_eq!(geometry("tkl").unwrap().slots.len(), 87);
        assert_eq!(geometry("full").unwrap().slots.len(), 104);
        assert_eq!(geometry("ortho").unwrap().slots.len(), 48);
    }

    #[test]
    fn iso_enter_is_tall() {
        // the ISO enter is the distinguishing 1.25u-wide, 2u-tall key
        let g = geometry("iso60").unwrap();
        assert!(g.slots.iter().any(|s| s.key.as_deref() == Some("enter") && s.h == 2.0));
    }

    #[test]
    fn no_exactly_overlapping_slots() {
        // two caps must not share a top-left corner (a transcription smoke test)
        for b in list() {
            let g = geometry(b.id).unwrap();
            for i in 0..g.slots.len() {
                for j in (i + 1)..g.slots.len() {
                    let (a, c) = (&g.slots[i], &g.slots[j]);
                    assert!(
                        a.x != c.x || a.y != c.y,
                        "{}: slots {i} and {j} overlap at ({}, {})",
                        b.id, a.x, a.y
                    );
                }
            }
        }
    }

    #[test]
    fn guess_matches_filename() {
        assert_eq!(guess("/etc/keyd/hhkb.conf"), "hhkb");
        assert_eq!(guess("planck.conf"), "ortho");
        assert_eq!(guess("my-tkl.conf"), "tkl");
        assert_eq!(guess("default.conf"), "ansi60");
        assert_eq!(guess("board-65.conf"), "65");
    }
}
